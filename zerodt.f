      subroutine zerodt(   nel,    ig,   det )
c
      implicit none
c
      integer nel,ig
c
      double precision det
c
c **********************************************************************
      WRITE(*,9000) nel, ig, det
c
c **********************************************************************
 9000 FORMAT('************************************************',/,
     &       '************************************************',/,
     &       '      Determinant becomes ZERO!! in :',i8,i5,e13.5)
c
c **********************************************************************
c **********************************************************************
      STOP 'Zero or Negative Determinant'
      END
